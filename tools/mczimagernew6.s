; MCZIMAGER_NEW.S
; PORTED FROM MCZIMAGER.S BY LES BIRD (2021)
; PORTED TO NEW BOARD WITH UPDATED I/O ADDRESSES
;
	ORG	6000H
;
; ORIGINAL CONSOLE: SERDAT=0DEH, SERCON=0DFH, RXRDY=BIT 1, TXRDY=BIT 0
; NEW CONSOLE:      SERDAT=092H, SERCON=093H, RXRDY=BIT 0, TXRDY=BIT 2
;
; ORIGINAL DISK:    DSKDAT=0CFH, DSSTAT/DSKCOM=0D0H, DSKSEL=0D1H
; NEW DISK:         DSKDAT=0CFH, DSSTAT/DSKCOM=0D0H, DSKSEL=0D1H (UNCHANGED)
;
; READS ZILOG MCZ DISKS TRACK BY TRACK AND SENDS BINARY DATA TO CONSOLE
; WRITES ZILOG MCZ DISKS TRACK BY TRACK FROM CONSOLE BINARY DATA
;
; WAIT FOR 'R' TO BEGIN READING DISK FROM FLOPPY
; WAIT FOR 'W' TO BEGIN WRITING DISK TO FLOPPY
;
; === NEW CONSOLE I/O ADDRESSES ===
SERDAT:	EQU	092H		; WAS 0DEH
SERCON:	EQU	093H		; WAS 0DFH
RXRDY:	EQU	0		; WAS 1
TXRDY:	EQU	2		; WAS 0
; === DISK I/O ADDRESSES (PER NEW BOARD SPEC) ===
DSKDAT:	EQU	0CFH		; DISK SHIFT REGISTER
DSKCOM:	EQU	0D0H		; DISK CONTROL A (ACTIVE OUTPUT)
DSSTAT:	EQU	0D0H		; DISK CONTROL A (READ STATUS)
DSKSEL:	EQU	0D1H		; DISK CONTROL B
CLK0:	EQU	0D4H
;       CONTROL WORDS
REST:	EQU	03
STCLK0:	EQU	10110111B
CTRMOD:	EQU	11010111B
TRIG0:	EQU	10101111B
RSTCLK:	EQU	00001011B
RDDAT:	EQU	04H		; DSKCOM 0000.0100
WRDAT:	EQU	08H		; DSKCOM 0000.1000
WRCRC:	EQU	18H
HDSTEP:	EQU	1		; DSKCOM 0000.0010
DIRECT:	EQU	0		; DSKCOM 0000.0001
READY:	EQU	5		; DSSTAT 0001.0000
T0:	EQU	6		; DSSTAT 0010.0000
CMP:	EQU	7
CRC:	EQU	7		; DSSTAT 0100.0000
WRTPTC:	EQU	7		; DSKSEL 0100.0000
;       TIMING CONSTANTS
TIM7:	EQU	67
SECT:	EQU	50
MS08:	EQU	77
MS18:	EQU	172
TIM256:	EQU	61
TM256A:	EQU	61
TIM512:	EQU	73
;
INTPNT:	EQU	12AFH
DSKVSL:	EQU	13D0H
FLOPPY:	EQU	0BFDH
PTRS:	EQU	12B4H
;
;
;
START:
	LD	(SAVSTK),SP	; SAVE RIO RETURN STACK
	LD	SP,STACK
	LD	A,13H
	LD	I,A
	IM	2		; ENSURE INTERRUPT MODE 2
	EI			; FLOPPY ROUTINE REQUIRES INTERRUPTS ENABLED
;
	LD	HL,MSG1
	LD	DE,MSGEND
	CALL	SENDHL
BEGIN:
	LD	HL,PROMPT
	LD	DE,PROLEN
	CALL	SENDHL
	CALL 	TTYIN		; WAIT FOR COMMAND
	AND	7FH		; STRIP BIT 7 (PARITY/HIGH BIT)
	RES	5,A		; FORCE UPPERCASE (MATCHES PROM CONVENTION)
	CP	'Q'
	JP	Z,QUIT		; QUIT THE PROGRAM RETURN TO OS
	CP	'G'		; FAST GET
	JP	Z,READ0		; DIRECT READ
	CP	'R'		; NORMAL READ
	JP	Z,READ3		; PROM READ
	CP	'W'		; NORMAL WRITE
	JP	Z,WRIT0		; PROM WRITE
	CP	'F'		; FAST WRITE
	JP	Z,WRIT3		; DIRECT WRITE VERSION
	CP	'D'		; DUMP SINGLE SECTOR
	JP	Z,DUMP1		; READ TRACK 23 SECTOR 3 -> HEX
	CP	'B'		; BURN SINGLE SECTOR
	JP	Z,BURN1		; HEX INPUT -> WRITE TRACK 23 SECTOR 3
	CP	'O'		; OVERWRITE FULL DISK FROM HEX
	JP	Z,OVWRT		; HEX INPUT -> WRITE ALL TRACKS
	JP	BEGIN
QUIT:
	LD	A,0DH
	CALL	TTYOUT
	LD	A,0AH
	CALL	TTYOUT
	LD	SP,(SAVSTK)
	RET
;
; DIRECT READ BYPASS PROM
; OUTPUT IN INTEL HEX FORMAT FOR 7-BIT TTY CAPTURE
;
READ0:
	CALL	SELDSK
	CALL	STRK0		; SETS (CURTRK) TO 0
	LD	HL,0
	LD	(HEXADR),HL	; RESET HEX ADDRESS COUNTER
READ1:
	CALL	RDTRK
	LD	HL,DATA
	LD	DE,1100H
	CALL	SNDHEX		; SEND TRACK AS INTEL HEX RECORDS
	LD	A,(CURTRK)
	CP	76		; LAST TRACK?
	JR	Z,READ2
	CALL	HDSTP1		; INCREMENTS (CURTRK)
	JR	READ1
READ2:
	CALL	HEXEOF		; SEND EOF RECORD
	CALL	DESDSK
	JR	BEGIN
;
; SEND BUFFER AT HL, LENGTH DE, AS INTEL HEX RECORDS (16 BYTES EACH)
;
SNDHEX:
	DI
SNDH1:
	LD	A,D		; CHECK IF LESS THAN 16 BYTES LEFT
	OR	A
	JR	NZ,SNDH2	; >255 LEFT, DO FULL 16
	LD	A,E
	OR	A
	JR	Z,SNDH3	; DONE
	CP	16
	JR	NC,SNDH2	; >=16 LEFT
; LAST PARTIAL RECORD
	LD	B,A		; B = REMAINING BYTE COUNT
	CALL	HEXREC
	JR	SNDH3
SNDH2:
	LD	B,16		; 16 BYTES PER RECORD
	CALL	HEXREC
; SUBTRACT 16 FROM DE
	LD	A,E
	SUB	16
	LD	E,A
	JR	NC,SNDH1
	DEC	D
	JR	SNDH1
SNDH3:
	EI
	RET
;
; OUTPUT ONE INTEL HEX RECORD
; B = BYTE COUNT, HL = DATA POINTER, (HEXADR) = LOAD ADDRESS
; FORMAT: :LLAAAATT[DD...]CC
;
HEXREC:
	PUSH	DE
	XOR	A
	LD	(HEXSUM),A	; CLEAR CHECKSUM
; SEND ':'
	LD	A,':'
	CALL	TTYOUT
; SEND BYTE COUNT
	LD	A,B
	CALL	HEXBYT
; SEND ADDRESS HIGH
	LD	A,(HEXADR+1)
	CALL	HEXBYT
; SEND ADDRESS LOW
	LD	A,(HEXADR)
	CALL	HEXBYT
; SEND RECORD TYPE 00 (DATA)
	XOR	A
	CALL	HEXBYT
; SEND DATA BYTES
	LD	C,B		; SAVE COUNT
HEXR1:
	LD	A,(HL)
	INC	HL
	CALL	HEXBYT
	DJNZ	HEXR1
; SEND CHECKSUM (TWO'S COMPLEMENT)
	LD	A,(HEXSUM)
	NEG
	CALL	HEXBYT
; SEND CR LF
	LD	A,0DH
	CALL	TTYOUT
	LD	A,0AH
	CALL	TTYOUT
; UPDATE ADDRESS
	LD	A,(HEXADR)
	ADD	A,C
	LD	(HEXADR),A
	JR	NC,HEXR2
	LD	A,(HEXADR+1)
	INC	A
	LD	(HEXADR+1),A
HEXR2:
	POP	DE
	RET
;
; SEND EOF RECORD :00000001FF
;
HEXEOF:
	LD	A,':'
	CALL	TTYOUT
	XOR	A
	LD	(HEXSUM),A
	CALL	HEXBYT		; 00 BYTE COUNT
	CALL	HEXBYT		; 00 ADDRESS HIGH
	CALL	HEXBYT		; 00 ADDRESS LOW
	LD	A,01H
	CALL	HEXBYT		; 01 RECORD TYPE (EOF)
	LD	A,(HEXSUM)
	NEG
	CALL	HEXBYT		; CHECKSUM
	LD	A,0DH
	CALL	TTYOUT
	LD	A,0AH
	CALL	TTYOUT
	RET
;
; OUTPUT BYTE IN A AS TWO HEX ASCII CHARS, ACCUMULATE CHECKSUM
;
HEXBYT:
	PUSH	AF
; ADD TO CHECKSUM
	LD	D,A
	LD	A,(HEXSUM)
	ADD	A,D
	LD	(HEXSUM),A
	LD	A,D
; HIGH NIBBLE
	RRCA
	RRCA
	RRCA
	RRCA
	CALL	HEXNIB
; LOW NIBBLE
	POP	AF
	CALL	HEXNIB
	RET
;
; OUTPUT LOW NIBBLE OF A AS HEX ASCII
;
HEXNIB:
	AND	0FH
	CP	10
	JR	C,HEXN1
	ADD	A,'A'-10	; A-F
	JR	HEXN2
HEXN1:
	ADD	A,'0'		; 0-9
HEXN2:
	CALL	TTYOUT
	RET
;
HEXADR:	DEFW	0		; CURRENT HEX RECORD ADDRESS
HEXSUM:	DEFB	0		; RUNNING CHECKSUM
;
; =====================================================
; 'D' COMMAND - DUMP TRACK 23 SECTOR 3 AS INTEL HEX
; =====================================================
;
DUMP1:
	CALL	SELDSK
	CALL	STRK0		; SEEK TO TRACK 0
; STEP TO TRACK 23
	LD	B,23
DUMP2:
	CALL	HDSTP1
	DJNZ	DUMP2
; READ ENTIRE TRACK INTO DATA BUFFER
	CALL	RDTRK
; FIND SECTOR 3 IN THE BUFFER (136 BYTES PER SECTOR)
; SECTOR BYTE IS AT OFFSET 0 OF EACH 136-BYTE BLOCK
; SECTOR 3 WITH BIT 7 SET = 83H
	LD	HL,DATA
	LD	B,32		; 32 SECTORS TO SEARCH
DUMP3:
	LD	A,(HL)
	AND	1FH		; MASK TO SECTOR NUMBER
	CP	3		; IS IT SECTOR 3?
	JR	Z,DUMP4
; ADVANCE TO NEXT SECTOR (136 BYTES = 88H)
	LD	DE,136
	ADD	HL,DE
	DJNZ	DUMP3
; SECTOR NOT FOUND - PRINT ERROR
	PUSH	HL
	LD	HL,MSGNSF
	LD	DE,NSFLEN
	CALL	SENDHL
	POP	HL
	CALL	DESDSK
	JP	BEGIN
;
DUMP4:
; HL POINTS TO THE 136-BYTE SECTOR RECORD
; OUTPUT AS INTEL HEX: 136 BYTES STARTING AT ADDRESS 0000
	PUSH	HL
	LD	HL,0
	LD	(HEXADR),HL
	POP	HL
	LD	DE,136		; 136 BYTES (SEC+TRK+DATA+BCK+FWD+CRC)
	DI
	CALL	SNDHEX
	CALL	HEXEOF
	EI
	CALL	DESDSK
	JP	BEGIN
;
MSGNSF:	DEFB	0DH,0AH
	DEFM	'SECTOR NOT FOUND'
	DEFB	0DH,0AH
NSFLEN:	EQU	$-MSGNSF
;
; =====================================================
; 'B' COMMAND - BURN TRACK 23 SECTOR 3 FROM INTEL HEX
; =====================================================
;
BURN1:
; FIRST READ THE INTEL HEX DATA FROM TTY INTO DATA BUFFER
	LD	HL,DATA
	LD	(HEXPTR),HL
;
BURN2:
; WAIT FOR ':' TO START A RECORD
	CALL	TTYIN
	AND	7FH
	CP	':'
	JR	NZ,BURN2
; INIT CHECKSUM FOR THIS RECORD
	XOR	A
	LD	(HEXSUM),A
; READ BYTE COUNT
	CALL	RXHEXB
	CALL	ADDHXS		; ADD BYTE COUNT TO CHECKSUM
	LD	B,A		; B = BYTE COUNT
	OR	A
	JR	Z,BURN5		; BYTE COUNT 0 = EOF RECORD
; READ ADDRESS (2 BYTES - IGNORED FOR POSITIONING)
	CALL	RXHEXB
	CALL	ADDHXS		; ADDR HIGH
	CALL	RXHEXB
	CALL	ADDHXS		; ADDR LOW
; READ RECORD TYPE
	CALL	RXHEXB
	CALL	ADDHXS
	CP	01H
	JR	Z,BURN5		; EOF RECORD
; READ DATA BYTES
	LD	HL,(HEXPTR)
BURN3:
	CALL	RXHEXB
	CALL	ADDHXS
	LD	(HL),A
	INC	HL
	DJNZ	BURN3
	LD	(HEXPTR),HL
; READ AND VERIFY CHECKSUM
	CALL	RXHEXB
	CALL	ADDHXS
	LD	A,(HEXSUM)
	OR	A
	JR	Z,BURN2		; CHECKSUM OK, NEXT RECORD
; CHECKSUM ERROR
	PUSH	HL
	LD	HL,MSGCSE
	LD	DE,CSELEN
	CALL	SENDHL
	POP	HL
	CALL	FLUSH		; DRAIN REMAINING TTY INPUT
	JP	BEGIN
;
BURN5:
; ALL HEX DATA RECEIVED - NOW WRITE TO DISK
; DATA BUFFER SHOULD CONTAIN 136 BYTES:
; SEC(1) TRK(1) DATA(128) BCK(2) FWD(2) CRC(2)
;
; FORCE SECTOR=3 TRACK=23 IN THE BUFFER
	LD	HL,DATA
	LD	A,3
	OR	80H		; SECTOR 3 WITH BIT 7
	LD	(HL),A
	INC	HL
	LD	A,23		; TRACK 23
	LD	(HL),A
;
	CALL	SELDSK
	CALL	STRK0		; SEEK TO TRACK 0
; STEP TO TRACK 23
	LD	B,23
BURN6:
	CALL	HDSTP1
	DJNZ	BURN6
; WRITE THE SECTOR USING PROM (SINGLE SECTOR WRITE)
	LD	A,0EH		; BINARY WRITE
	LD	(SCREQ),A
	LD	A,3
	LD	(SCSEC),A
	LD	A,23
	LD	(SCTRK),A
	LD	HL,DATA+2	; DATA STARTS AFTER SEC+TRK
	LD	(SCDTA),HL
	LD	HL,128
	LD	(SCDL),HL
; SET BCK/FWD POINTERS FROM BUFFER
	LD	HL,DATA+130	; BCK1 AT OFFSET 130
	LD	A,(HL)
	LD	(PTRS+0),A
	INC	HL
	LD	A,(HL)
	LD	(PTRS+1),A
	INC	HL
	LD	A,(HL)
	LD	(PTRS+2),A
	INC	HL
	LD	A,(HL)
	LD	(PTRS+3),A
;
	LD	IY,SCVEC
	CALL	FLOPPY		; WRITE SECTOR
;
	CALL	DESDSK
; CONFIRM
	LD	HL,MSGWOK
	LD	DE,WOKLEN
	CALL	SENDHL
	CALL	FLUSH		; DRAIN REMAINING TTY INPUT
	JP	BEGIN
;
; READ TWO HEX ASCII CHARS FROM TTY, RETURN BYTE IN A
;
RXHEXB:
	CALL	RXHNIB		; HIGH NIBBLE
	RLCA
	RLCA
	RLCA
	RLCA
	LD	D,A
	CALL	RXHNIB		; LOW NIBBLE
	OR	D
	RET
;
; READ ONE HEX ASCII CHAR FROM TTY, RETURN VALUE IN A (0-15)
;
RXHNIB:
	CALL	TTYIN
	AND	7FH
	CP	'0'
	JR	C,RXHN1		; BELOW '0' = ERROR, TREAT AS 0
	CP	'9'+1
	JR	C,RXHN2		; '0'-'9'
	RES	5,A		; FORCE UPPERCASE
	CP	'A'
	JR	C,RXHN1
	CP	'F'+1
	JR	C,RXHN3		; 'A'-'F'
RXHN1:
	XOR	A		; ERROR: RETURN 0
	RET
RXHN2:
	SUB	'0'
	RET
RXHN3:
	SUB	'A'-10
	RET
;
; ADD A TO CHECKSUM
;
ADDHXS:
	PUSH	AF
	LD	E,A
	LD	A,(HEXSUM)
	ADD	A,E
	LD	(HEXSUM),A
	POP	AF
	RET
;
HEXPTR:	DEFW	0		; POINTER FOR HEX RECEIVE
;
MSGCSE:	DEFB	0DH,0AH
	DEFM	'CHECKSUM ERROR'
	DEFB	0DH,0AH
CSELEN:	EQU	$-MSGCSE
;
MSGWOK:	DEFB	0DH,0AH
	DEFM	'SECTOR WRITTEN OK'
	DEFB	0DH,0AH
WOKLEN:	EQU	$-MSGWOK
;
; =====================================================
; 'O' COMMAND - OVERWRITE FULL DISK FROM INTEL HEX INPUT
; EXPECTS SAME FORMAT AS G OUTPUT: 77 TRACKS X 1100H BYTES
; RECEIVES ONE TRACK (1100H BYTES) THEN WRITES IT, REPEATS
; =====================================================
;
OVWRT:
	CALL	SELDSK
	CALL	STRK0		; SEEK TO TRACK 0
	XOR	A
	LD	(CURTRK),A
;
OVWR1:
; RECEIVE ONE TRACK OF HEX DATA INTO DATA BUFFER
	LD	HL,DATA
	LD	(HEXPTR),HL
	LD	HL,0
	LD	(OVRCNT),HL	; BYTES RECEIVED THIS TRACK
;
OVWR2:
; WAIT FOR ':' TO START A RECORD
	CALL	TTYIN
	AND	7FH
	CP	':'
	JR	NZ,OVWR2
; INIT CHECKSUM
	XOR	A
	LD	(HEXSUM),A
; READ BYTE COUNT
	CALL	RXHEXB
	CALL	ADDHXS
	LD	B,A
	OR	A
	JR	Z,OVWR6		; BYTE COUNT 0 = EOF RECORD -> DONE
; READ ADDRESS (2 BYTES - IGNORED, WE FILL SEQUENTIALLY)
	CALL	RXHEXB
	CALL	ADDHXS
	CALL	RXHEXB
	CALL	ADDHXS
; READ RECORD TYPE
	CALL	RXHEXB
	CALL	ADDHXS
	CP	01H
	JR	Z,OVWR6		; EOF RECORD -> DONE
; READ DATA BYTES
	LD	HL,(HEXPTR)
OVWR3:
	CALL	RXHEXB
	CALL	ADDHXS
	LD	(HL),A
	INC	HL
	DJNZ	OVWR3
	LD	(HEXPTR),HL
; READ AND VERIFY CHECKSUM
	CALL	RXHEXB
	CALL	ADDHXS
	LD	A,(HEXSUM)
	OR	A
	JR	NZ,OVWR7	; CHECKSUM ERROR
; UPDATE BYTE COUNT FOR THIS TRACK
	LD	HL,(HEXPTR)
	LD	DE,DATA
	OR	A		; CLEAR CARRY
	SBC	HL,DE		; HL = BYTES RECEIVED SO FAR
	LD	(OVRCNT),HL
; CHECK IF WE HAVE A FULL TRACK (1100H BYTES)
	LD	DE,1100H
	OR	A
	SBC	HL,DE
	JR	C,OVWR2		; NOT YET - KEEP READING
;
; FULL TRACK RECEIVED - WRITE IT
OVWR4:
	CALL	WRTRK1		; DIRECT WRITE TRACK
; PRINT TRACK NUMBER AS PROGRESS
	LD	A,(CURTRK)
	CALL	PRHEX2		; PRINT 2 HEX DIGITS
	LD	A,' '
	CALL	TTYOUT
; ADVANCE TO NEXT TRACK
	LD	A,(CURTRK)
	CP	76		; LAST TRACK?
	JR	Z,OVWR5		; YES - FINISH
	CALL	HDSTP1		; STEP TO NEXT TRACK
	JP	OVWR1		; RECEIVE NEXT TRACK
;
OVWR5:
; ALL TRACKS WRITTEN
	CALL	DESDSK
	LD	HL,MSGODN
	LD	DE,ODNLEN
	CALL	SENDHL
	CALL	FLUSH
	JP	BEGIN
;
OVWR6:
; EOF RECEIVED - WRITE ANY REMAINING PARTIAL TRACK
	LD	HL,(OVRCNT)
	LD	A,H
	OR	L
	JR	Z,OVWR5		; NOTHING LEFT TO WRITE
; WRITE WHATEVER WE HAVE (PARTIAL LAST TRACK)
	JR	OVWR4
;
OVWR7:
; CHECKSUM ERROR
	CALL	DESDSK
	LD	HL,MSGCSE
	LD	DE,CSELEN
	CALL	SENDHL
	CALL	FLUSH
	JP	BEGIN
;
; PRINT A AS 2 HEX ASCII DIGITS (NO CHECKSUM)
;
PRHEX2:
	PUSH	AF
	RRCA
	RRCA
	RRCA
	RRCA
	CALL	HEXNIB
	POP	AF
	CALL	HEXNIB
	RET
;
OVRCNT:	DEFW	0		; BYTES RECEIVED FOR CURRENT TRACK
;
MSGODN:	DEFB	0DH,0AH
	DEFM	'DISK WRITE COMPLETE'
	DEFB	0DH,0AH
ODNLEN:	EQU	$-MSGODN
;
; =====================================================
;
; PROM READ
;
READ3:
	LD	A,0AH
	LD	(SCREQ),A	; BINARY READ
	XOR	A
	LD	(SCTRK),A
READ4:
	XOR	A
	LD	(SCSEC),A
	LD	HL,DATA
	LD	(SCDTA),HL
; READ 32 SECTORS USING PROM
	PUSH	HL
	CALL	RDTRK7
	POP	HL
; SEND DATA TO CONSOLE
	LD	DE,1100H
	CALL	SENDHL
;
	LD	A,(SCTRK)
	INC	A
	LD	(SCTRK),A
	CP	77
	JR	NZ,READ4
	JP	BEGIN
RDTRK7:
	LD	A,(SCSEC)
	OR	80H
	LD	(HL),A		; PUT SECTOR
	INC	HL
	LD	A,(SCTRK)
	LD	(HL),A		; PUT TRACK
	INC	HL
	PUSH	HL
	LD	(SCDTA),HL
	LD	HL,128
	LD	(SCDL),HL
	LD	IY,SCVEC
	CALL	FLOPPY		; READ DATA
	POP	HL
	LD	DE,128
	ADD	HL,DE		; ADVANCE DATA POINTER
	LD	DE,PTRS
	LD	A,(DE)		; GET BCK1
	INC	DE
	LD	(HL),A		; PUT BCK1
	INC	HL
	LD	A,(DE)		; GET BCK2
	INC	DE
	LD	(HL),A		; PUT BCK2
	INC	HL
	LD	A,(DE)		; GET FWD1
	INC	DE
	LD	(HL),A		; PUT FWD1
	INC	HL
	LD	A,(DE)		; GET FWD2
	INC	DE
	LD	(HL),A		; PUT FWD2
	INC	HL
	XOR	A
	LD	(HL),A		; CRC1 - NOT USED
	INC	HL
	LD	(HL),A		; CRC2 - NOT USED
	INC	HL
	LD	A,(SCSEC)
	INC	A
	LD	(SCSEC),A
	CP	32
	JR	NZ,RDTRK7
	RET
;
SENDHL:
	DI
SENDH1:
	LD	A,(HL)
	INC	HL
	CALL	TTYOUT
	DEC	DE
	LD	A,D
	OR	E
	JR	NZ,SENDH1
;
	EI
	RET
;
; WRITE DISK USING PROM
;
WRIT0:
	LD	A,0EH		; BINARY WRITE
	LD	(SCREQ),A
	XOR	A
	LD	(CURTRK),A
WRIT1:
	CALL	SENDRDY
	LD	HL,DATA
	LD	DE,1100H
	CALL	READHL
	CALL	WRTRK
	LD	A,(CURTRK)
	INC	A
	LD	(CURTRK),A
	CP	77
	JR	NZ,WRIT1
WRIT2:
	CALL	SENDRDY
	JP	BEGIN
;
; DIRECT WRITE WITHOUT USING PROM
; MODIFIED: USES DUMMY TEST PATTERN INSTEAD OF TTY INPUT
;
WRIT3:
	CALL	SELDSK
	CALL	STRK0
WRIT4:
;	CALL	SENDRDY		; SKIP TTY HANDSHAKE FOR DUMMY MODE
;	LD	HL,DATA
;	LD	DE,1100H
;	CALL	READHL		; SKIP TTY READ - USE DUMMY DATA INSTEAD
	CALL	FILDAT		; FILL DATA BUFFER WITH TEST PATTERN
	CALL	WRTRK1
	LD	A,(CURTRK)
	CP	76
	JR	Z,WRIT5
	CALL	HDSTP1		; INCREMENTS CURTRK
	JR	WRIT4
WRIT5:
	CALL	DESDSK
;	CALL	SENDRDY
	JP	BEGIN
;
; FILL DATA BUFFER WITH DUMMY TEST PATTERN
; FORMAT PER SECTOR: SEC(1) TRK(1) DATA(128) BCK(2) FWD(2) CRC(2) = 136 BYTES
;
FILDAT:
	LD	HL,DATA
	XOR	A
	LD	(FLSEC),A	; SECTOR COUNTER
FILD1:
	LD	A,(FLSEC)
	OR	80H		; SECTOR BYTE WITH BIT 7 SET
	LD	(HL),A
	INC	HL
	LD	A,(CURTRK)	; TRACK NUMBER
	LD	(HL),A
	INC	HL
; FILL 128 BYTES OF DATA WITH PATTERN
	LD	B,128
	LD	A,(FLSEC)	; USE SECTOR NUMBER AS SEED
FILD2:
	LD	(HL),A
	INC	HL
	INC	A
	DJNZ	FILD2
; BCK POINTER (2 BYTES)
	XOR	A
	LD	(HL),A		; BCK1
	INC	HL
	LD	(HL),A		; BCK2
	INC	HL
; FWD POINTER (2 BYTES)
	LD	(HL),A		; FWD1
	INC	HL
	LD	(HL),A		; FWD2
	INC	HL
; CRC (2 BYTES)
	LD	(HL),A		; CRC1
	INC	HL
	LD	(HL),A		; CRC2
	INC	HL
;
	LD	A,(FLSEC)
	INC	A
	LD	(FLSEC),A
	CP	32		; ALL 32 SECTORS?
	JR	NZ,FILD1
	RET
;
FLSEC:	DEFB	0		; TEMP SECTOR COUNTER FOR FILDAT
;
READHL:
	DI
READH1:
	CALL	TTYIN
	LD	(HL),A
	INC	HL
	DEC	DE
	LD	A,D
	OR	E
	JR	NZ,READH1
;
	EI
	RET
;
SENDRDY:
	LD	A,'R'
	CALL	TTYOUT
	RET
;
; SEEK TO TRACK 0
;
STRK0:
	XOR	A
	LD	(CURTRK),A
STRK1:
	IN	A,(DSSTAT)	; CHECK THE TRACK 0 LINE.
	BIT	T0,A		; IS IT THERE?
	RET	Z
	CALL	HDSTP0
	JR	STRK1
;
; SELECT DISK 0
; NEW BOARD: DRIVE 0 = BIT 0 (PROM TABLE AT 0B1BH SHOWS 01H)
; ORIGINAL MCZ USED BIT 3 FOR DRIVE 0
;
SELDSK:
	LD	A,RSTCLK
	OUT	(CLK0),A
	LD	A,0
	SET	0,A		; BIT 0 = DRIVE 0 ON NEW BOARD (WAS BIT 3)
	OUT	(DSKSEL),A
;	IN	A,(DSSTAT)	; CHECK AND MAKE SURE THE
;	BIT	READY,A		; SELECTED DISK IS READY.
	RET
;
; DESELECT DISK 0
;
DESDSK:
	LD	A,RSTCLK
	OUT	(CLK0),A
	IN	A,(DSKSEL)	; FIND OUT WHICH DISK IS SELECTED.
	BIT	0,A		; SEE IF DRIVE 0 IS SELECTED (WAS BIT 3)
	RET	Z
	AND	06H		; MASK OFF DRIVE 0 BIT (WAS AND 07H)
	OUT	(DSKSEL),A	; DESELECT IT.
	RET
;
; STEP HEAD
;
HDSTP0:
	LD	B,0		; SET TO STEP TOWARDS TRACK 0
	JR	HDSTP3
HDSTP1:
	LD	A,(CURTRK)
	CP	76
	RET	Z
HDSTP2:
	INC	A
	LD	(CURTRK),A
	LD	B,0
	SET	DIRECT,B	; SET TO STEP TOWARDS TRACK 77
HDSTP3:
	LD	C,DSKCOM
	OUT	(C),B
	SET	HDSTEP,B
	OUT	(C),B		; OUTPUT THE STEP PULSE.
	EX	(SP),HL		; DELAY
	EX	(SP),HL
	RES	HDSTEP,B	; OUTPUT STEP TRAILING EDGE.
	OUT	(C),B
	CALL	HDDLY0
	RET
;
; DIRECT READ TRACK
;
RDTRK:
	LD	HL,DATA
	LD	(DATPTR),HL
	XOR	A
	LD	(CURSEC),A
; WAIT FOR SECTOR HOLE
RDTRK0:
	XOR	A
	LD	(SECFLG),A
;
	LD	HL,SCVEC
	LD	(DSKVSL),HL
;
	LD	HL,RDTRK2	; SET HANDLER BEFORE ARMING CTC
	LD	(INTPNT),HL
	LD	A,CTRMOD	; INTERRUPT AT NEXT SECTOR.
	OUT	(CLK0),A
	LD	A,1
	OUT	(CLK0),A	; CTC NOW ARMED - INTPNT ALREADY VALID
; LOOP UNTIL SECTOR FLAG IS SET
RDTRK1:
	LD	A,(SECFLG)
	OR	A
	JR	Z,RDTRK1
	LD	HL,0
	LD	(DSKVSL),HL
	JR	RDTRK3
; INTERRUPT TO SET THE SECTOR FLAG
RDTRK2:
	LD	A,80H
	LD	(SECFLG),A
	LD	(SCCC),A
	RET
RDTRK3:
	DI
	LD	HL,(DATPTR)
	LD	A,(CURSEC)
	OR	80H
	LD	E,A
	LD	A,RDDAT		; PREPARE TO READ.
	LD	B,TM256A
	DJNZ	$		; TIME OUT 256 USEC BEFORE READING.
	OUT	(DSKCOM),A	; TURN ON READ CIRCUITRY.
	LD	BC,8000H+DSKDAT	; 128 BYTE SECTOR + SEC + TRK + BCK + FWD + CRC
; 400 USECS FROM INTERRUPT
	IN	A,(DSKDAT)	; READ SECTOR
	CP	E
	IN	A,(DSKDAT)	; READ TRACK	
	JR	NZ,RDTRK6
	LD	(HL),E		; STORE SECTOR
	INC	HL
	LD	(HL),A		; STORE TRACK
	INC	HL
RDTRK4:
	INI			; DATA
	NOP
	NOP
	NOP
	JR	NZ,RDTRK4
; BCK/FWD
	INI
	INI
	INI
	INI
; CRC
	INI
	INI
	LD	(DATPTR),HL
RDTRK5:
;	TURN OFF READ
	XOR	A
	OUT	(DSKCOM),A
	EI
	LD	A,(CURSEC)
	INC	A
	LD	(CURSEC),A
	CP	32		; LAST SECTOR?
	JP	NZ,RDTRK0	; NO? READ NEXT ONE
	RET			; ELSE RETURN
RDTRK6:
	EI
	XOR	A
	OUT	(DSKCOM),A	; TURN OFF READ
	JP	RDTRK0
;
; WRITE SECTOR USING PROM - REQUIRES FORMATTED DISK
;
WRTRK:
	LD	HL,DATA
	XOR	A
	LD	(CURSEC),A
;
WRTRK0:
	LD	IY,SCVEC
	LD	A,(HL)		; GET SECTOR
	AND	1FH
	LD	(SCSEC),A
	INC	HL
	LD	A,(HL)		; GET TRACK
	LD	(SCTRK),A
	INC	HL
	LD	(SCDTA),HL	; SET DATA POINTER
;
	LD	DE,80H		; SKIP 128 BYTES
	ADD	HL,DE		; JUMP TO BCK,FWD POINTER VALUES
;
	LD	A,(HL)		; BCK1
	LD	(PTRS+0),A
	INC	HL
	LD	A,(HL)		; BCK2
	LD	(PTRS+1),A
	INC	HL
	LD	A,(HL)		; FWD1
	LD	(PTRS+2),A
	INC	HL
	LD	A,(HL)		; FWD2
	LD	(PTRS+3),A
	INC	HL
;	
	PUSH	HL
	LD	HL,128
	LD	(SCDL),HL
	CALL	FLOPPY		; WRITE SECTOR
	POP	HL
;
	INC	HL		; CRC1
	INC	HL		; CRC2
;
	LD	A,(CURSEC)
	INC	A
	LD	(CURSEC),A
	CP	32		; LAST SECTOR WRITTEN?
	JP	NZ,WRTRK0
	RET
;
; DIRECT WRITE BYPASS PROM
;
WRTRK1:
	XOR	A
	LD	(CURSEC),A
;
; WAIT FOR INDEX HOLE
;
	CALL	WAIDX
;
	LD	HL,DATA
	LD	(DATPTR),HL
WRTRK3:
	CALL	WASEC		; WAIT FOR SECTOR HOLE
;
	DI
;
	LD	A,WRDAT
	OUT	(DSKCOM),A	; WRITE ON
;
	LD	BC,8200H+DSKDAT
	LD	HL,(DATPTR)
;	LD	A,TIM512
	LD	A,58		; DELAY 928 TSECS
	DEC	A
	JR	NZ,$-1
	NOP
	NOP
; HERE AT 1288 TSECS (VS 1287 IN PROM)
	OUTI			; SECTOR
	OUTI			; TRACK
	NOP
	NOP
WRTRK5:				; WRITE 128 BYTES OF DATA
	OUTI
	NOP
	NOP
	NOP
	JR	NZ,WRTRK5
;
	OUTI			; BCK1
	OUTI			; BCK2
	OUTI			; FWD1
	LD	A,WRCRC
	OUTI			; FWD2
;
;	OUTI			; CRC1
;	OUTI			; CRC2
;
	OUT	(DSKCOM),A
	OUT	(DSKDAT),A	; CRC1
	OUT	(DSKDAT),A	; CRC2
;
	XOR	A
	OUT	(DSKDAT),A	; POSTAMBLE1
	OUT	(DSKDAT),A	; POSTAMBLE2
	OUT	(DSKCOM),A	; WRITE OFF
;
	INC	HL		; CRC1
	INC	HL		; CRC2
	LD	(DATPTR),HL
;
	EI
;
	LD	A,(CURSEC)
	INC	A
	LD	(CURSEC),A
	CP	32
	JP	NZ,WRTRK3
	RET
;
; WAIT FOR INDEX HOLE
;
; 5 MS PER SECTOR HOLE
; O...O...O...O.I.O...O...O...O...ETC
;28  29  30  31 ^ 0   1   2   3
;
WAIDX:
	CALL	WASEC		; WAIT AT LEAST ONE SECTOR
WAIDX1:
	LD	DE,SECFLG
;
	XOR	A
	LD	(SECFLG),A
	LD	(SCCC),A
;
	LD	HL,SCVEC
	LD	(DSKVSL),HL
;
	LD	HL,RDTRK2	; SET HANDLER BEFORE ARMING CTC
	LD	(INTPNT),HL
	LD	A,CTRMOD	; INTERRUPT AT NEXT SECTOR.
	OUT	(CLK0),A
	LD	A,1
	OUT	(CLK0),A	; CTC NOW ARMED
	LD	BC,0
WAIDX2:
	INC	BC
	LD	A,(DE)
	OR	A
	JR	Z,WAIDX2
	LD	A,B
	CP	01H		; LESS THAN 01H IS AN INDEX MARKER
	JR	NC,WAIDX1
; INDEX HOLE LOCATED
	LD	HL,0
	LD	(DSKVSL),HL
	RET
;
WASEC:
	LD	DE,SECFLG
;
	XOR	A
	LD	(SECFLG),A
	LD	(SCCC),A
;
	LD	HL,SCVEC
	LD	(DSKVSL),HL
;
	LD	HL,RDTRK2	; SET HANDLER BEFORE ARMING CTC
	LD	(INTPNT),HL
	LD	A,CTRMOD	; INTERRUPT AT NEXT SECTOR.
	OUT	(CLK0),A
	LD	A,1
	OUT	(CLK0),A	; CTC NOW ARMED
; LOOP UNTIL SECTOR FLAG IS SET
WASEC1:
	LD	A,(DE)
	OR	A
	JR	Z,WASEC1
;
	LD	HL,0
	LD	(DSKVSL),HL
	RET
;
; HEAD STEP DELAY
;
HDDLY0:
	LD	DE,0400H
HDDLY1:				; DELAY LOOP BEFORE MOVE HEAD AGAIN
	DEC	DE
	LD	A,D
	OR	E
	JP	NZ,HDDLY1
	RET
;
; READ BYTE FROM CONSOLE
; ACTIVE HIGH: BIT 0 = 1 WHEN DATA AVAILABLE (MATCHES PROM AT 0F57H)
;
TTYIN:	IN 	A,(SERCON)
	BIT	RXRDY,A	; BIT 0 = RX READY (1=DATA AVAILABLE)
	JR	Z,TTYIN		; LOOP WHILE BIT IS CLEAR
	IN	A,(SERDAT)
	RET
;
; WRITE BYTE TO CONSOLE
; ACTIVE HIGH: BIT 2 = 1 WHEN TX READY (MATCHES PROM AT 0F6BH)
;
TTYOUT:	
	PUSH	AF
TTYOU1:
	IN	A,(SERCON)
	BIT	TXRDY,A	; BIT 2 = TX READY (1=READY TO SEND)
	JR	Z,TTYOU1
	POP	AF
	OUT	(SERDAT),A
	RET
;
; FLUSH TTY INPUT - DRAIN ANY REMAINING CHARACTERS
; WAITS UNTIL NO CHARACTER RECEIVED FOR ~50MS
;
FLUSH:
FLUSH1:
	LD	DE,0800H	; TIMEOUT COUNTER (~50MS AT 4MHZ)
FLUSH2:
	IN	A,(SERCON)
	BIT	RXRDY,A	; CHARACTER AVAILABLE?
	JR	NZ,FLUSH3	; YES - READ AND DISCARD IT
	DEC	DE
	LD	A,D
	OR	E
	JR	NZ,FLUSH2	; KEEP WAITING
	RET			; TIMEOUT - INPUT IS DRAINED
FLUSH3:
	IN	A,(SERDAT)	; READ AND DISCARD
	JR	FLUSH1		; RESET TIMEOUT
;
;
